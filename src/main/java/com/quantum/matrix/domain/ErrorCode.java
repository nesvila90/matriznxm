package com.quantum.matrix.domain;

/**
 * The enum Error code.
 */
public enum ErrorCode {

    /**
     * Data corrupt exception error code.
     */
    DATA_CORRUPT_EXCEPTION,
    /**
     * Zero size parameter exception error code.
     */
    ZERO_SIZE_PARAMETER_EXCEPTION,
    /**
     * Incorrect format error code.
     */
    INCORRECT_FORMAT,
    /**
     * General exception error code.
     */
    GENERAL_EXCEPTION,
    /**
     * Matrix dimension are not quadratic error code.
     */
    MATRIX_DIMENSION_ARE_NOT_QUADRATIC;

}
